using namespace std;
#include<iostream>
int main()
{
    int a=40;

     switch(a)
     {
         case 10:
         cout <<"case 10 is stored in a";
         break;
         
         case 20:
         cout<<"case 20 is stored in a";
         break;
         
         case 40:
         cout<<"case 40 is stored in a";
         break;
         
         default:
         cout<<"no case is matching";
         break;
     }
    return 0;
}
